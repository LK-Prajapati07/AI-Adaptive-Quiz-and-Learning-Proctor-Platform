import mongoose from 'mongoose'
const questionResultSchema=new mongoose.Schema({
    question:{
        type:String
    },
    studentAnswer:{
        type:String
    },
    isCorrect:{
        type:String
    },
    obtainedMarks:{
        type:Number
    },
    totalMarks:{
        type:Number
    },
    feedback:{
        type:String
    }
},
{
    _id:false
}

)
const resultSchema=new mongoose.Schema({
    userId:{
        type:mongoose.Schema.Types.ObjectId,
        ref:"Auth",
        required:true
    },
    quizId:{
        type:mongoose.Schema.Types.ObjectId,
        ref:"Quiz",
        required:true
    },
    attemptId:{
        type:mongoose.Schema.Types.ObjectId,
        ref:"Attempt",
        required:true
    },
    questionType:{
        type:String,
        requires:true
    },
    totalQuestion:{
        type:Number,

    },
    atttemptQuestions:{
        type:Number
    },
    totalMarks:{
        type:Number
    },
    obtainedMarks:{
        type:Number
    },
    percentage:{
        type:Number
    },
    status:{
        type:String,
        enum:['Pass','Fail'],
        default:null
    },
    results:[
        questionResultSchema
    ]
},
{
    timestamps:true
}
)
export const Result=mongoose.model('Result',resultSchema)